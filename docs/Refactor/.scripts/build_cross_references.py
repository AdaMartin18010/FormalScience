def get_all_md_files() -> List[Path]:
    """获取所有需要处理的markdown文件"""
    md_files = []
    for section in MAIN_SECTIONS:
        section_path = DOCS_ROOT / section
        if section_path.exists():
            md_files.extend(section_path.rglob("*.md"))
    return sorted(md_files)

def extract_keywords(content: str) -> Set[str]:
    """从文档内容提取关键词"""
    keywords = set()
    for concept in CONCEPT_MAP.keys():
        if concept in content:
            keywords.add(concept)
    return keywords

def find_related_docs(doc_path: Path, keywords: Set[str], all_files: List[Path]) -> List[Tuple[str, str]]:
    """根据关键词找到相关文档"""
    related = []
    doc_rel_path = doc_path.relative_to(DOCS_ROOT).as_posix()
    
    for keyword in keywords:
        for rel_path in CONCEPT_MAP.get(keyword, []):
            if rel_path != doc_rel_path:
                # 找到对应的标题
                target_path = DOCS_ROOT / rel_path
                if target_path.exists():
                    title = extract_title(target_path)
                    related.append((rel_path, title or keyword))
    
    # 去重并限制数量
    seen = set()
    unique_related = []
    for rel_path, title in related:
        if rel_path not in seen and len(unique_related) < 5:
            seen.add(rel_path)
            unique_related.append((rel_path, title))
    
    return unique_related[:5]

def extract_title(doc_path: Path) -> str:
    """提取文档标题"""
    try:
        with open(doc_path, 'r', encoding='utf-8') as f:
            for line in f:
                line = line.strip()
                if line.startswith('# '):
                    return line[2:].strip()
                elif line.startswith('## '):
                    return line[3:].strip()
    except:
        pass
    return None

def get_prerequisites(doc_path: Path) -> List[Tuple[str, str]]:
    """获取文档的前置知识"""
    rel_path = doc_path.relative_to(DOCS_ROOT).as_posix()
    prereqs = PREREQUISITE_MAP.get(rel_path, [])
    
    result = []
    for prereq_path in prereqs:
        target_path = DOCS_ROOT / prereq_path
        if target_path.exists():
            title = extract_title(target_path)
            result.append((prereq_path, title or prereq_path))
    
    return result

def generate_prerequisite_section(prereqs: List[Tuple[str, str]]) -> str:
    """生成前置知识章节"""
    if not prereqs:
        return ""
    
    lines = ["\n---\n", "## 📋 前置知识\n"]
    for rel_path, title in prereqs:
        lines.append(f"- [{title}](./{rel_path})")
    lines.append("")
    return "\n".join(lines)

def generate_related_section(related: List[Tuple[str, str]]) -> str:
    """生成延伸阅读章节"""
    if not related:
        return ""
    
    lines = ["\n---\n", "## 📚 延伸阅读\n"]
    for rel_path, title in related:
        lines.append(f"- [{title}](./{rel_path})")
    lines.append("")
    return "\n".join(lines)

def process_document(doc_path: Path, all_files: List[Path]) -> bool:
    """处理单个文档，添加交叉引用"""
    try:
        with open(doc_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # 检查是否已经处理过
        if "📋 前置知识" in content and "📚 延伸阅读" in content:
            return False  # 已处理，跳过
        
        # 提取关键词
        keywords = extract_keywords(content)
        
        # 获取前置知识
        prereqs = get_prerequisites(doc_path)
        
        # 获取相关文档
        related = find_related_docs(doc_path, keywords, all_files)
        
        # 如果没有前置知识和相关文档，跳过
        if not prereqs and not related:
            return False
        
        # 准备添加的内容
        new_sections = ""
        
        # 在前置知识章节后添加（如果还没有）
        if prereqs and "📋 前置知识" not in content:
            # 尝试在标题后添加
            prereq_section = generate_prerequisite_section(prereqs)
            new_sections += prereq_section
        
        # 在文末添加延伸阅读
        if related and "📚 延伸阅读" not in content:
            related_section = generate_related_section(related)
            new_sections += related_section
        
        if not new_sections:
            return False
        
        # 写入文件
        with open(doc_path, 'w', encoding='utf-8') as f:
            f.write(content.rstrip() + new_sections)
        
        return True
    except Exception as e:
        print(f"Error processing {doc_path}: {e}")
        return False

def main():
    """主函数"""
    print("开始建立文档交叉引用网络...")
    
    # 获取所有markdown文件
    all_files = get_all_md_files()
    print(f"找到 {len(all_files)} 个文档")
    
    processed = 0
    skipped = 0
    
    for doc_path in all_files:
        if process_document(doc_path, all_files):
            processed += 1
            if processed % 50 == 0:
                print(f"  已处理 {processed} 个文档...")
        else:
            skipped += 1
    
    print(f"\n完成！")
    print(f"  - 新处理: {processed} 个文档")
    print(f"  - 已存在/跳过: {skipped} 个文档")
    print(f"  - 总计: {len(all_files)} 个文档")

if __name__ == "__main__":
    main()
