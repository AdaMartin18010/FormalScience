def extract_title_from_file(file_path):
    """从文件内容中提取标题"""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
        # 提取第一个一级标题
        match = re.search(r'^#\s+(.+)$', content, re.MULTILINE)
        if match:
            return match.group(1).strip()
        # 如果没有一级标题，使用文件名
        return Path(file_path).stem.replace('_', ' ')

def extract_topic_from_path(file_path):
    """从文件路径中提取主题"""
    parts = Path(file_path).parts
    if len(parts) >= 2:
        return parts[-2]  # 父目录名
    return "调度系统"

