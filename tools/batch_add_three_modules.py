def process_file(file_path):
    """处理单个文件"""
    print(f"Processing: {file_path}")
    
    # 检查文件是否已包含三大模块
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
        if '思维表征体系' in content and '理论体系' in content and '关联网络' in content:
            print(f"  ✓ Already has three modules, skipping")
            return False
    
    # 提取标题和主题
    title = extract_title_from_file(file_path)
    topic = extract_topic_from_path(file_path)
    
    # 生成三大模块内容
    three_modules = generate_three_modules(file_path, title, topic)
    
    # 在文件末尾（返回链接之前）插入三大模块
    # 查找返回链接的位置
    return_pattern = r'(\n---\s*\n.*?最后更新.*?\n)'
    match = re.search(return_pattern, content, re.DOTALL)
    
    if match:
        # 在返回链接之前插入
        insert_pos = match.start()
        new_content = content[:insert_pos] + '\n' + three_modules + content[insert_pos:]
    else:
        # 如果没有返回链接，在文件末尾添加
        new_content = content + '\n' + three_modules
    
    # 写回文件
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    print(f"  ✓ Added three modules")
    return True

def main():
    """主函数"""
    base_dir = Path('Composed/schedule_formal_view')
    
    # 获取所有需要处理的文件
    files = []
    for md_file in base_dir.rglob('*.md'):
        # 只处理编号文件（如01.1_xxx.md）
        if re.match(r'^\d+\.\d+_', md_file.name):
            files.append(md_file)
    
    print(f"Found {len(files)} files to process")
    print()
    
    processed = 0
    skipped = 0
    
    for file_path in sorted(files):
        if process_file(file_path):
            processed += 1
        else:
            skipped += 1
    
    print()
    print(f"Summary:")
    print(f"  Processed: {processed}")
    print(f"  Skipped: {skipped}")
    print(f"  Total: {len(files)}")

if __name__ == '__main__':
    main()
